package first;

public class Variable {

	public static void main(String[] args) {
		int count;
		
		count = 10;
		
		count = 20;
		
//		마지막에 들어간 값이 담김
		
		double avg = 11.1;
		
		String name = "Jami";
		
//		변수는 첫번째 문자가 항상 소문자로 사용된다.
//		변수의 두단어 이상이 사용될때는 두번째 단어의 첫글자는 대문자로 사용
		
		System.out.println(count);
		System.out.println(avg);
		System.out.println(name);
	}

}
