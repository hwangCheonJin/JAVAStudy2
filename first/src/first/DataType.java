package first;

public class DataType {

	public static void main(String[] args) {
		boolean isFun = true;
		System.out.println(isFun);
//		boolean형은 true, false 두가지 값만 가능하다.
		
		char c = 'f';
		System.out.println(c);
//		char형은 단어 하나만 가능하다.
		
		int x = 59;
		
		long bing = 3435343343L;
		
		float f = 32.4F;
		
		double d = 342535.3;
	}

}
